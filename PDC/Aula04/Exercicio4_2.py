# Exercício 4.2-) Crie um algoritmo que imprima na tela os
# números ímpares de 1 a 50.

# Abertura laço de repetição até 50
for i in range(50):
    # Somatória no contador
    i = i + 1
    # Verificação se número é ímpar
    if i % 2 == 1:
        # Impressão do número ímpar
        print(i)